export let currentLang = localStorage.getItem('pixelArenaLang') || 'en';

export function t(str) {
  if (currentLang === 'en') return str;
  if (translations[currentLang] && translations[currentLang][str]) {
    return translations[currentLang][str];
  }
  const prefixes = ["Score: ", "High Score: ", "Time: ", "Level: ", "Lines: ", "Round ", "Level ", "Question ", "Lives: ", "Points: "];
  for (let p of prefixes) {
    if (str.startsWith(p)) {
      const transPrefix = translations[currentLang][p] || p;
      const rest = str.substring(p.length);
      const transRest = translations[currentLang][rest.trim()] || rest;
      return transPrefix + transRest;
    }
  }
  return str;
}

export function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('pixelArenaLang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  if (lang === 'ar') {
    document.documentElement.style.setProperty('--font-primary', '"Cairo", "Outfit", sans-serif');
  } else {
    document.documentElement.style.setProperty('--font-primary', '"Outfit", sans-serif');
  }
  translateDOM(document.body);
}

const originalTextMap = new WeakMap();

function translateNode(node) {
  let originalText = originalTextMap.get(node);
  if (originalText === undefined) {
    originalText = node.nodeValue;
    originalTextMap.set(node, originalText);
  }
  const trimmed = originalText.trim();
  if (trimmed && !/^[\d\W]+$/.test(trimmed)) {
    const trans = t(trimmed);
    node.nodeValue = originalText.replace(trimmed, trans);
  }
}

function translateDOM(root) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
  let node;
  while(node = walker.nextNode()) {
    if (node.parentElement && (node.parentElement.tagName === 'SCRIPT' || node.parentElement.tagName === 'STYLE')) continue;
    translateNode(node);
  }
  root.querySelectorAll('input').forEach(input => {
    let pOriginal = originalTextMap.get(input);
    if (pOriginal === undefined && input.placeholder) {
      pOriginal = input.placeholder;
      originalTextMap.set(input, pOriginal);
    }
    if (pOriginal) input.placeholder = t(pOriginal);
  });
}

export function initI18n() {
  document.documentElement.lang = currentLang;
  document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
  const originalFillText = CanvasRenderingContext2D.prototype.fillText;
  CanvasRenderingContext2D.prototype.fillText = function(text, x, y, maxWidth) {
    return originalFillText.call(this, t(String(text)), x, y, maxWidth);
  };
  const observer = new MutationObserver(mutations => {
    mutations.forEach(mutation => {
      mutation.addedNodes.forEach(node => {
        if (node.nodeType === Node.ELEMENT_NODE) translateDOM(node);
        else if (node.nodeType === Node.TEXT_NODE) {
          if (node.parentElement && node.parentElement.tagName !== 'SCRIPT' && node.parentElement.tagName !== 'STYLE') {
            translateNode(node);
          }
        }
      });
    });
  });
  observer.observe(document.body, { childList: true, subtree: true });
  translateDOM(document.body);
  
  window.dispatchEvent(new Event('languageChanged'));
}
